    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        // TODO add your handling code here:
        String roll = this.roll.getText();
        String subject1 = this.s1.getText();
        String subject2 = this.s2.getText();
        String subject3 = this.s3.getText();
        String subject4 = this.s4.getText();
        String subject5 = this.s5.getText();
        String marks1 = this.m1.getText();
        String marks2 = this.m2.getText();
        String marks3 = this.m3.getText();
        String marks4 = this.m4.getText();
        String marks5 = this.m5.getText();
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms", "root", "Yash@1234");
            Statement st = con.createStatement();
            
            ResultSet rs = st.executeQuery("SELECT * FROM student WHERE rollno ='"+roll+"'");
            
            if(rs.next()){
                st.executeUpdate("INSERT INTO result(rollno, subject1,subject2,subject3,subject4,subject5, marks1,marks2,marks3,marks4,marks5) values('"+roll+"', '"+subject1+"','"+subject2+"','"+subject3+"','"+subject4+"','"+subject5+"','"+marks1+"','"+marks2+"','"+marks3+"','"+marks4+"','"+marks5+"')");
                JOptionPane.showMessageDialog(null, "Data saved successfully!");
                setVisible(false);
                new addResult().setVisible(true);
            }else{
                JOptionPane.showMessageDialog(null, "This roll number is not registered!");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_addActionPerformed

