    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        // TODO add your handling code here:
        String course = (String) this.course.getSelectedItem();
        String branchName = (String) this.branchName.getSelectedItem();
        String roll = this.rollNo.getText();
        String name = this.name.getText();
        String gender = (String) this.gender.getSelectedItem();
        String fatherName = this.fathername.getText();
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms", "root", "Yash@1234");
            
            Statement st = con.createStatement();
            st.executeUpdate("INSERT INTO student(rollno, course, branchName, name, gender, fathername) values('"+roll+"','"+course+"','"+branchName+"','"+name+"','"+gender+"','"+fatherName+"')");
            JOptionPane.showMessageDialog(null, "Data saved successfully!");
            setVisible(false);
            new adminHome().setVisible(true);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());   
        }
    }//GEN-LAST:event_addActionPerformed

